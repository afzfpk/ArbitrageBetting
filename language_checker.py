    @staticmethod
    def check_text(text: str) -> Dict:
        """
        Verifica se o texto está em português correcto.
        
        Args:
            text: Texto para verificar
            
        Returns:
            Relatório de verificação
        """
        issues = []
        suggestions = []
        
        # Verificar palavras do Brasil
        words = re.findall(r'\b\w+\b', text.lower())
        
        for word in words:
            if word in LanguageChecker.BRAZIL_TO_PORTUGAL:
                issues.append(f"'{word}' → '{LanguageChecker.BRAZIL_TO_PORTUGAL[word]}'")
        
        # Verificar inglês desnecessário
        english_words = []
        for word in words:
            # Ignorar palavras técnicas permitidas
            if (word.isalpha() and 
                len(word) > 2 and 
                word not in LanguageChecker.ALLOWED_ENGLISH and
                not word.startswith('http') and
                re.match(r'^[a-zA-Z]+$', word)):
                
                # Verificar se é provável inglês (heurística simples)
                if (word.endswith('ing') or 
                    word.endswith('tion') or 
                    word.startswith('un') or
                    word in ['the', 'and', 'for', 'are', 'you', 'can', 'have', 'this', 'that']):
                    english_words.append(word)
        
        # Verificações específicas PT-PT
        pt_issues = []
        
        # Uso correcto de "tu" vs "você"
        if 'você' in text and 'tu' not in text:
            suggestions.append("Considere usar 'tu' em vez de 'você' para PT-PT informal")
        
        # Termos técnicos em português
        if 'smartphone' in text.lower():
            suggestions.append("'smartphone' → 'telemóvel inteligente'")
        
        if 'tablet' in text.lower():
            suggestions.append("'tablet' → 'tablet' (OK) ou 'táblet'")
        
        # Acentuação portuguesa
        if 'aplicacao' in text.lower():
            issues.append("'aplicacao' → 'aplicação'")
        
        if 'configuracao' in text.lower():
            issues.append("'configuracao' → 'configuração'")
        
        return {
            "total_words": len(words),
            "issues_found": len(issues) + len(english_words),
            "brazil_corrections": issues,
            "english_words": english_words,
            "suggestions": suggestions,
            "score": max(0, 100 - (len(issues) + len(english_words)) * 2)
        }
    
